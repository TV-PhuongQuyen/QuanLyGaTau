    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        TauDAO dao  = new TauDAO();
        String name = request.getParameter("ten");
        String sohieutau = request.getParameter("sohieu");
        String trangthai = request.getParameter("trangthai");
       
          String message;
        if (dao.kiemTraSoHieuTau(sohieutau)) {
            message = "Số hiệu tàu đã tồn tại. Vui lòng kiểm tra lại!";
        } else {
            dao.ThemTau(name, sohieutau, trangthai);
            message = "Thêm tàu thành công!";
        }
        response.sendRedirect("TauServlet");
    } 

